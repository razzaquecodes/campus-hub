CREATE TABLE IF NOT EXISTS public.student_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    roll_number TEXT NOT NULL,
    semester TEXT NOT NULL,
    sgpa NUMERIC(4,2),
    cgpa NUMERIC(4,2),
    backlog BOOLEAN DEFAULT FALSE,
    raw_result_status TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    UNIQUE(roll_number, semester)
);

-- Enable RLS
ALTER TABLE public.student_results ENABLE ROW LEVEL SECURITY;

-- Policy: Students can view their own results
CREATE POLICY "Students can view their own results"
ON public.student_results
FOR SELECT
TO authenticated
USING (roll_number = (auth.jwt() ->> 'roll_number'));

-- Optional: If the frontend uses the raw `roll_number` in a different way or JWT doesn't have it,
-- you might allow all authenticated users to read. Assuming roll_number is checked.
-- Policy: Service role can manage all
CREATE POLICY "Service role can manage all"
ON public.student_results
FOR ALL
TO service_role
USING (true)
WITH CHECK (true);
